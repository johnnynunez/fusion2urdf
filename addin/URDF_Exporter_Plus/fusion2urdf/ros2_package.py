"""ROS 2 description package generator (targets Jazzy + Gazebo Sim).

Layout produced::

    <robot>_description/
        package.xml            (ament_cmake)
        CMakeLists.txt
        urdf/<robot>.xacro     + materials.xacro + <robot>.ros2control
        meshes/*.stl           (copied from the export dir)
        launch/display.launch.py
        launch/gazebo.launch.py
        rviz/display.rviz
        config/ros_gz_bridge.yaml
"""

from __future__ import annotations

import shutil
from pathlib import Path

from .model import Robot
from .urdf_writer import (
    build_materials_xacro,
    build_ros2_control_xacro,
    build_xacro_main,
)

PACKAGE_XML = """\
<?xml version="1.0"?>
<?xml-model href="http://download.ros.org/schema/package_format3.xsd" schematypens="http://www.w3.org/2001/XMLSchema"?>
<package format="3">
  <name>{package}</name>
  <version>0.1.0</version>
  <description>URDF description of {robot}, generated by fusion2urdf</description>
  <maintainer email="user@example.com">fusion2urdf</maintainer>
  <license>MIT</license>

  <buildtool_depend>ament_cmake</buildtool_depend>

  <exec_depend>robot_state_publisher</exec_depend>
  <exec_depend>joint_state_publisher</exec_depend>
  <exec_depend>joint_state_publisher_gui</exec_depend>
  <exec_depend>rviz2</exec_depend>
  <exec_depend>xacro</exec_depend>
  <exec_depend>ros_gz_sim</exec_depend>
  <exec_depend>ros_gz_bridge</exec_depend>

  <export>
    <build_type>ament_cmake</build_type>
  </export>
</package>
"""

CMAKELISTS = """\
cmake_minimum_required(VERSION 3.8)
project({package})

find_package(ament_cmake REQUIRED)

install(
  DIRECTORY urdf meshes launch rviz config
  DESTINATION share/${{PROJECT_NAME}}
)

ament_package()
"""

DISPLAY_LAUNCH = '''\
"""Display {robot} in RViz2 with joint_state_publisher_gui."""

import os

import xacro
from ament_index_python.packages import get_package_share_directory
from launch import LaunchDescription
from launch.actions import DeclareLaunchArgument
from launch.conditions import IfCondition, UnlessCondition
from launch.substitutions import LaunchConfiguration
from launch_ros.actions import Node


def generate_launch_description():
    share_dir = get_package_share_directory("{package}")

    xacro_file = os.path.join(share_dir, "urdf", "{robot}.xacro")
    robot_urdf = xacro.process_file(xacro_file).toxml()

    rviz_config = os.path.join(share_dir, "rviz", "display.rviz")

    gui_arg = DeclareLaunchArgument(name="gui", default_value="True")
    show_gui = LaunchConfiguration("gui")

    return LaunchDescription(
        [
            gui_arg,
            Node(
                package="robot_state_publisher",
                executable="robot_state_publisher",
                parameters=[{{"robot_description": robot_urdf}}],
            ),
            Node(
                condition=UnlessCondition(show_gui),
                package="joint_state_publisher",
                executable="joint_state_publisher",
            ),
            Node(
                condition=IfCondition(show_gui),
                package="joint_state_publisher_gui",
                executable="joint_state_publisher_gui",
            ),
            Node(
                package="rviz2",
                executable="rviz2",
                arguments=["-d", rviz_config],
                output="screen",
            ),
        ]
    )
'''

GAZEBO_LAUNCH = '''\
"""Spawn {robot} in Gazebo Sim (gz-sim, ROS 2 Jazzy)."""

import os

import xacro
from ament_index_python.packages import get_package_share_directory
from launch import LaunchDescription
from launch.actions import IncludeLaunchDescription, TimerAction
from launch.launch_description_sources import PythonLaunchDescriptionSource
from launch_ros.actions import Node


def generate_launch_description():
    pkg_ros_gz_sim = get_package_share_directory("ros_gz_sim")
    share_dir = get_package_share_directory("{package}")

    xacro_file = os.path.join(share_dir, "urdf", "{robot}.xacro")
    robot_description = {{"robot_description": xacro.process_file(xacro_file).toxml()}}
    bridge_config = os.path.join(share_dir, "config", "ros_gz_bridge.yaml")

    gazebo = IncludeLaunchDescription(
        PythonLaunchDescriptionSource(
            os.path.join(pkg_ros_gz_sim, "launch", "gz_sim.launch.py")
        ),
        launch_arguments={{"gz_args": "-r -v 4 empty.sdf"}}.items(),
    )

    robot_state_publisher = Node(
        package="robot_state_publisher",
        executable="robot_state_publisher",
        output="screen",
        parameters=[robot_description],
    )

    # Delayed spawn with renaming disabled to avoid duplicate entities.
    spawn_robot = TimerAction(
        period=5.0,
        actions=[
            Node(
                package="ros_gz_sim",
                executable="create",
                arguments=[
                    "-topic", "/robot_description",
                    "-name", "{robot}",
                    "-allow_renaming", "false",
                    "-z", "0.1",
                ],
                output="screen",
            )
        ],
    )

    ros_gz_bridge = Node(
        package="ros_gz_bridge",
        executable="parameter_bridge",
        parameters=[{{"config_file": bridge_config}}],
        output="screen",
    )

    return LaunchDescription(
        [gazebo, robot_state_publisher, spawn_robot, ros_gz_bridge]
    )
'''

