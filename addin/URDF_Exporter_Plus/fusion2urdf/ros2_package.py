def generate_ros2_package(
    robot: Robot,
    output_dir: str | Path,
    meshes_dir: str | Path | None = None,
    package_name: str | None = None,
) -> Path:
    """Write a complete ROS 2 description package. Returns the package path."""
    robot.validate()
    package = package_name or f"{robot.name}_description"
    pkg_dir = Path(output_dir) / package

    for sub in ("urdf", "meshes", "launch", "rviz", "config"):
        (pkg_dir / sub).mkdir(parents=True, exist_ok=True)

    fmt = {"package": package, "robot": robot.name}
    (pkg_dir / "package.xml").write_text(PACKAGE_XML.format(**fmt), encoding="utf-8")
    (pkg_dir / "CMakeLists.txt").write_text(CMAKELISTS.format(**fmt), encoding="utf-8")

    (pkg_dir / "urdf" / f"{robot.name}.xacro").write_text(
        build_xacro_main(robot, package), encoding="utf-8"
    )
    (pkg_dir / "urdf" / "materials.xacro").write_text(
        build_materials_xacro(robot), encoding="utf-8"
    )
    (pkg_dir / "urdf" / f"{robot.name}.ros2control.xacro").write_text(
        build_ros2_control_xacro(robot), encoding="utf-8"
    )

    (pkg_dir / "launch" / "display.launch.py").write_text(
        DISPLAY_LAUNCH.format(**fmt), encoding="utf-8"
    )
    (pkg_dir / "launch" / "gazebo.launch.py").write_text(
        GAZEBO_LAUNCH.format(**fmt), encoding="utf-8"
    )
    (pkg_dir / "rviz" / "display.rviz").write_text(RVIZ_CONFIG, encoding="utf-8")
    (pkg_dir / "config" / "ros_gz_bridge.yaml").write_text(
        BRIDGE_CONFIG, encoding="utf-8"
    )

    if meshes_dir:
        meshes_dir = Path(meshes_dir)
        for link in robot.links:
            if not link.mesh:
                continue
            src = meshes_dir / link.mesh
            if src.exists():
                shutil.copy2(src, pkg_dir / "meshes" / src.name)

    return pkg_dir
